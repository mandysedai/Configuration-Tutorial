package frc.robot;
public class FrameSize {
    private float width;
    private float length;
    private float bumperThickness;

    public float getWidth() {
        return width;
    }
    public void setWidth(float width) {
        this.width = width;
    }

    public float getLength() {
        return length;
    }
    public void setLength(float length) {
        this.length = length;
    }

    public float getBumperThickness() {
        return bumperThickness;
    }
    public void setBumperThickness(float bumperThickness) {
        this.bumperThickness = bumperThickness;
    }
  }